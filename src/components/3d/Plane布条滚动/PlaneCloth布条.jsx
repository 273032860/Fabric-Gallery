const PlaneCloth布条 = ({path, position, curve, offset = 0, direction = 1}) => {
  const meshRef = useRef()
  const [hovered, setHovered] = useState(false)
  
  // 1. 创建稳定的 Uniform 引用 (类似参考代码中的 tt)
  const uHtmlTexture = useMemo(() => uniformTexture(new Texture()), []);

  // 创建 Flow 实例来生成曲线数据纹理
  const flow = useMemo(() => {
    if (!curve) return null;
    // 我们创建一个虚拟 monocle 传给 Flow，只是为了让它初始化纹理
    const dummy = new LineSegments();
    const f = new Flow(dummy);
    f.updateCurve(0, curve);
    return f;
  }, [curve]);

  // 更新位置 (CPU 同步，确保射线检测/事件正确)
  // 不使用 lookAt，让 Shader 处理所有旋转
  useFrame(() => {
    if (curve && meshRef.current) {
      const realOffset = ((offset * direction) % 1 + 1) % 1;
      const pos = curve.getPointAt(realOffset);
      meshRef.current.position.copy(pos);
    }
  });

  const texture = useTexture(path)
  texture.colorSpace = SRGBColorSpace
  texture.flipY = false
  const { nodes, controls } = useMemo(() => {
   
  const uBigWavesFrequencyX = uniform(1.0);
  const uBigWavesFrequencyY = uniform(1.0);
  const uBigWavesElevation = uniform(0.5);
  const uMeshPos = uniform(new Vector3());
  const uPathOffset = uniform(offset % 1);
  const uZoom = uniform(vec3(1, 1, 1));
  const uUvOffsetY = uniform(0.7);

  const wave = Fn(([progress, localY]) => {
    // 使用 progress (0-1，表示在曲线上的进度) 替代世界坐标 X
    // 这样多块布条只要在曲线上重合，它们的波浪相位就完全一致
    const x = progress.mul(10.0) // 缩放进度以调整波长
    const y = localY

    const freqX = uBigWavesFrequencyX
    const freqY = uBigWavesFrequencyY

    // 第一层波
    const wave1 = sin(
      x.mul(freqX)
        .add(y.mul(freqY))
        .add(time)
    )

    // 第二层波（带偏移 + 不同时间流速）
    const wave2 = sin(
      x.mul(freqX.add(1.0))
        .add(y.mul(freqY))
        .add(time.mul(0.7))
    )

    const smallWave = sin(
      x.mul(5.0)
        .add(time.mul(2.0))
    ).mul(0.1)

    // 合并（统一乘 amplitude）
    const elevation = wave1
      .add(wave2)
      .add(smallWave)
      .mul(uBigWavesElevation)

    return elevation
  })

  // 曲线变形节点
  const curveBendingNode = Fn(() => {
    if (!flow) return positionLocal;

    const TEXTURE_HEIGHT = 4;
    const numberOfCurves = 1;
    const textureStacks = TEXTURE_HEIGHT / 4;
    const textureScale = TEXTURE_HEIGHT * numberOfCurves;
      const spineTexture = flow.uniforms.spineTexture;
  
      // 计算当前顶点在曲线上的相对位置
      // positionLocal.x 是平面宽度方向的偏移（-4 到 4）
      // spineLength 是曲线的总长度（flow 会自动计算）
      const spineLength = reference('spineLength', 'float', flow.uniforms);
      const pathSegment = reference('pathSegment', 'float', flow.uniforms);
      
      // 关键点：我们让顶点在局部空间位移，但 lookup 基于曲线
      // 我们把 local.x 转换为曲线上的比例偏移
      const mt = positionLocal.x.div(spineLength).mul(pathSegment).add(uPathOffset).mul(textureStacks).toVar();
      mt.assign(mt.mod(textureStacks));
 
     const rowOffset = mt.floor().toVar();

    // 采样曲线位置
    const spinePos = textureNode(spineTexture, vec2(mt, rowOffset.add(0.5).div(textureScale))).xyz;
    
    // 采样基矩阵 (tangent, normal, binormal)
    const a = textureNode(spineTexture, vec2(mt, rowOffset.add(1.5).div(textureScale))).xyz;
    const b = textureNode(spineTexture, vec2(mt, rowOffset.add(2.5).div(textureScale))).xyz;
    const c = textureNode(spineTexture, vec2(mt, rowOffset.add(3.5).div(textureScale))).xyz;
    const basis = mat3(a, b, c);

    // 同样应用波浪变形
    const waveOffset = wave(mt, positionLocal.y);
    const localPosWithWave = vec3(0, positionLocal.y, waveOffset);

    // 将局部 y 和波浪偏移应用到基矩阵空间，然后加上曲线位置
     // 注意：我们要减去 Mesh 在 CPU 端的中心位置，因为 Shader 是在 local 空间计算的
     return basis.mul(localPosWithWave).add(spinePos).sub(uMeshPos);
   });

   const positionNode = curveBendingNode();
    // 实现缩放效果：将 uv 偏移到中心并缩放，再偏移回来
    const zoomedUv = uv().sub(0.5).div(uZoom.xy).add(0.5);
    // const zoomedUv2 = uv()

    const rotatedUv = rotateUV(zoomedUv, MathUtils.degToRad(90));
    // const rotatedUv2 = rotateUV(zoomedUv2, MathUtils.degToRad(90)).add(vec2(0, uUvOffsetY));
    // const baseColor = textureNode(texture, rotatedUv);
    const baseColor = (textureNode(texture, rotatedUv))
    // const htmlColor = textureNode(uHtmlTexture, rotatedUv2);
     
     // 将 HTML 纹理混合到基础纹理上，使用 HTML 纹理的 alpha 通道作为混合系数
    //  const colorNode = mix(vec4(baseColor.rgb,1.0), htmlColor.rgb,htmlColor.a);
     const colorNode = baseColor
    //  const colorNode = ;
 
    return {
      nodes: {
        positionNode,
        colorNode
      },
      controls: {
         uBigWavesFrequencyX,
         uBigWavesFrequencyY,
         uBigWavesElevation,
         uMeshPos,
         uPathOffset,
         uZoom,
         uHtmlTexture,
         uUvOffsetY
       }
    }
   }, [texture, flow, uHtmlTexture]); // 依赖 flow 和 uHtmlTexture


   

  useGSAP(() => {
    if (!meshRef.current) return;
    gsap.to(controls.uZoom.value, {
      x: hovered ? 1.2 : 1, // 3.1 太大了，通常 1.5-2 比较合适
      y: hovered ? 1.2 : 1,
      z: hovered ? 1.2 : 1,
      duration: 0.4,
      ease: "power2.out"
    })
  }, [hovered])
  
    // 每帧同步 uniform
    useFrame(() => {
      if (meshRef.current && nodes.positionNode) {
        controls.uMeshPos.value.copy(meshRef.current.position);
        const realOffset = ((offset * direction) % 1 + 1) % 1;
        controls.uPathOffset.value = realOffset;
      }
    });

  useControls("布条波浪控制", {
    frequencyX: {
      value: 1.0,
      min: 0,
      max: 5,
      step: 0.1,
      onChange: (v) => { controls.uBigWavesFrequencyX.value = v }
    },
    frequencyY: {
      value: 1.0,
      min: 0,
      max: 5,
      step: 0.1,
      onChange: (v) => { controls.uBigWavesFrequencyY.value = v }
    },
    elevation: {
      value: 0.1,
      min: 0,
      max: 2,
      step: 0.01,
      onChange: (v) => { controls.uBigWavesElevation.value = v }
    },
    uvOffsetY: {
      value: -0.4,
      min: -1,
      max: 1,
      step: 0.01,
      onChange: (v) => { controls.uUvOffsetY.value = v }
    }
  })
  
  useCursor(hovered)
  return (
    <>
        <mesh 
          ref={meshRef}
          position={position} 
          onPointerOver={(e) => {
            e.stopPropagation()
            setHovered(true)
          }}
          onPointerOut={() => setHovered(false)}
          
        >
          <planeGeometry args={[4.5, 6,10,10]} />
          <meshBasicNodeMaterial {...nodes}  side={DoubleSide} transparent={true} wireframe={false} />
        </mesh>
    </>
  )
}



